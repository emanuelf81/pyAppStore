    private void JLImgAppMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JLImgAppMouseClicked
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Jpg, Gif & Png imagenes", "jpg", "gif", "png");
        chooser.setFileFilter(filter);
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            fileNvaImgApp = chooser.getSelectedFile();
            if (fileNvaImgApp.getName().endsWith("jpg") || this.fileNvaImgApp.getName().endsWith("png") || this.fileNvaImgApp.getName().endsWith("gif")) {
                fileImgApp = fileNvaImgApp;
            } else {
                fileNvaImgApp = null;
                JOptionPane.showMessageDialog(this, "Solo imágenes de 120 x 120px \ny con extensión .jpg, .png o .gif",
                        "Selección de Imagen", JOptionPane.ERROR_MESSAGE);
            }
            JLImgApp.setIcon(new ImageIcon(fileImgApp.getPath()));
        }
    }//GEN-LAST:event_JLImgAppMouseClicked

