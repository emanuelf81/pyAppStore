    private void JBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAceptarActionPerformed
        boolean band = true;
        String msjGestor = ""; // msj = "" --> OK / msj != "" --> Error gestores.

        // verifica que no falte ingresar información.
        if (JTFNomApp.getText().equals("") || JTFPrecioApp.getText().equals("")
                || JTADescripApp.getText().equals("")) {
            
            JOptionPane.showMessageDialog(this,
                    "Olvidó ingresar información.",
                    "Aplicaciones", JOptionPane.ERROR_MESSAGE);
            band = false;
        }
        if (JTADescripApp.getText().length()>250) {
            JOptionPane.showMessageDialog(this,
                    "Descripción demasiado extensa",
                    "Aplicaciones", JOptionPane.ERROR_MESSAGE);
            band = false;
        }

        // verifica si ha seleccionado una imagen para la aplicación.
        if (fileNvaImgApp == null && band == true) { // <--- No hay nueva imagen
            int rpt = JOptionPane.showConfirmDialog(this,
                    "No ha seleccionado una imagen para la aplicación.\nDesea hacerlo ahora?",
                    "Aplicaciones", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (rpt == 0) { // <--- YES
                band = false;
            }
        }

        // Guarda una aplicación en la tabla. 
        if (band) {
            msjGestor = gestorApp.nuevaAplicacion(JTFNomApp.getText(),
                    JTADescripApp.getText(), Double.valueOf(JTFPrecioApp.getText()),
                    us.getIdUs(), fileImgApp.getPath());

            if (msjGestor.equals("")) {
                JOptionPane.showMessageDialog(this, "Operación exitosa!!!");
                this.dispose();
                jfp.dispose();
                JFPrincipal p = new JFPrincipal(us);
                
            } else {
                JOptionPane.showMessageDialog(this, msjGestor,
                        "Aplicaciones", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_JBAceptarActionPerformed

