    private void JBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAceptarActionPerformed
        boolean band = true;
        if (calificacion == 0) {
            JOptionPane.showMessageDialog(this,
                    "Olvidó ingresar la calificación.",
                    "Calificar", JOptionPane.ERROR_MESSAGE);
        } else {
            if (JTAComent.getText().equals("")) {
                int rpt = JOptionPane.showConfirmDialog(this,
                        "No ha escrito un comentario.\nDesea hacerlo ahora?",
                        "Calificar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (rpt == 0) { // <--- YES
                    band = false;
                }
            }
            if (band) {
                String msjGestor = gestorCompra.calificarAppComprada(app.getIdApp(), us, calificacion, JTAComent.getText());
                if (msjGestor.equals("")) {
                    JOptionPane.showMessageDialog(this, "Operación exitosa!!!");
                    this.dispose();
                    jfp.dispose();
                    JFPrincipal p = new JFPrincipal(us);
                } else {
                    JOptionPane.showMessageDialog(this, msjGestor,
                            "Calificar", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_JBAceptarActionPerformed

