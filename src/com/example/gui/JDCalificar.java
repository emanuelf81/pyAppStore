    private void cargaRating() {
        // Carga en el array jl los JLabel de las estrellas
        // la posición [0] no la utilizo
        for (int i = 0; i < JPCalif.getComponentCount(); i++) {
            if (JPCalif.getComponent(i) instanceof JLabel) {
                jl[Integer.parseInt(JPCalif.getComponent(i).getName())] = (JLabel) JPCalif.getComponent(i);
            }
        }
    }

    private void estrellaSeleccionada(int e) {
        for (int i = 5; i > e; i--) {
            jl[i].setIcon(new ImageIcon("./star25.png"));
        }
        for (int i = 1; i <= e; i++) {
            jl[i].setIcon(new ImageIcon("./star25sel.png"));
        }
        calificacion = e;
    }

