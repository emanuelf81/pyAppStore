    private void JBComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBComprarActionPerformed
        int rpt = JOptionPane.showConfirmDialog(this,
                "Desea comprar la aplicaci√≥n " + app.getNomApp() + " ?",
                "Carrito de compras",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (rpt == 0) {
            if (gestorCompra.nuevaCompra(app.getIdApp(), us.getIdUs())) {
                JBComprar.setVisible(false);
                JBCalificar.setVisible(true);
                JOptionPane.showMessageDialog(this, "Operaci√≥n exitosa!!!");
                jfp.dispose();
                JFPrincipal p = new JFPrincipal(us);
            } else {
                JOptionPane.showMessageDialog(this, "La compra no se ha podido concretar.\nInt√©ntelo nuevamente.");
            }
        }
    }//GEN-LAST:event_JBComprarActionPerformed

